# Modules:
# are always written in lower case
# When multiple words are used, an underscore should separate them
# It's usually preferable to stick to 1 word names

# Classes:
# Names should follow UpperCaseCamelCase convention
# Python's built-in classes, however, are typically lowercase words
# Exception should end in "Error"

# Global Variables:
# Should be all lowercase
# Words in a global variable name should be separated by an underscore

# Instance Variables:
# Names should be all lowercase
# Words in an instance variable name should be separated by an underscore
# Non-public instance variables should begin with a single underscore
# For private variable, two underscores may begin its name

# Functions:
# Names should be all lowercase
# Words in a function name should be separated by an underscore
# Private method should bein with a double underscore

# Method argument:
# Instance methods should have their first argument named "self"
# lowercase for parameter names

# Constants
# Constant names myst be fully capitalized
# Words in a constant name should be separated by an underscore