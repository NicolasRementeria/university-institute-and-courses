monday_temperatures = [9.1, 8.8, 7.5]

# From 0 index to 0 index
monday_temperatures[0:1]

# Output:
# [9.1]

# From 0 index to 2 index
monday_temperatures[0:3]
# Output:
# [9.1, 8.8, 7.5]

# From 2 index to 2 index
monday_temperatures[2:3] 
# Output:
# [7.5]

# From 1 index to the end
monday_temperatures[1:]
# Output: 
# [8.8, 7.5]

# From the begining to the 1st index
monday_temperatures[:2]
# Output:
# [9.1, 8.8]