monday_temperatures = [9.1, 8.8, 7.5]

# Get the last index value
# NOTE: THIS IS THE VALUE OF THE LAST POSITION, NOT AN ARRAY OF THE LAST POSITION
monday_temperatures[-1]
# Output:
# 7.5

# Get a list of all the values of the list starting from the penultimate index
monday_temperatures[-2:]
# Output:
# [8.8, 7.5]

# Get a list of the 3rd index from right to left, to the 2nd from right to left
monday_temperatures[-3:-1]